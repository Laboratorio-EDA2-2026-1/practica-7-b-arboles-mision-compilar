#___________ Práctica 8 — B-Árboles ___________


#Importamos las operaciones implementadas de los otros archivos
from Insercion_B_arboles import insertar
from Busqueda_B_arboles import buscar
from Eliminacion_B_arboles import eliminar
from pprint import pprint  # Para mostrar el árbol más legible


#Crea un árbol vacío
t = 3  #Grado mínimo del B-árbol
arbol = {
    'raiz': {
        'claves': [],
        'hijos': [],
        'esHoja': True
    }
}

print("______________PRÁCTICA 8: OPERACIONES EN UN B-ÁRBOL______________\n")

# INSERCIÓN

valores = [10, 20, 5, 6, 12, 30, 7, 17]
print("Insertando valores en el B-árbol...")
for v in valores:
    insertar(arbol, v, t)

print("\n_____ B-ÁRBOL DESPUÉS DE LAS INSERCIONES _____")
pprint(arbol)

# BÚSQUEDA

print("\n - PRUEBAS DE BÚSQUEDA:")

for clave in [6, 15, 30]:
    if buscar(arbol['raiz'], clave):
        print(f"La clave {clave} SÍ se encuentra en el B-árbol.")
    else:
        print(f"La clave {clave} NO se encuentra en el B-árbol.")

# ELIMINACIÓN

print("\n - PRUEBAS DE ELIMINACIÓN:")

for clave in [6, 17, 10]:
    print(f"\nEliminando la clave {clave}...")
    eliminar(arbol, clave, t)
    pprint(arbol)

print("\n_____ FIN _____")
